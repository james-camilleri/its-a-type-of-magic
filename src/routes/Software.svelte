<script lang="ts">
  import { onMount } from 'svelte'
  import { tweened } from 'svelte/motion'

  const ONE_SECOND = 1000
  const bugAngle = tweened(0)
  const bugAngleReturn = tweened(0)
  const bugDistance = tweened(0)

  onMount(() => {
    moveBug()
  })

  function random(min: number, max: number) {
    return Math.random() * (max - min) + min
  }

  function randomSeconds(min: number, max: number) {
    return random(min, max) * ONE_SECOND
  }

  function moveBug() {
    const startTime = randomSeconds(5, 10)
    const crawlTime = randomSeconds(3, 4)
    const pauseTime = randomSeconds(0.5, 2)
    const crawlBackTime = randomSeconds(1.5, 2)
    const turnTime = randomSeconds(0.5, 2)
    const angle = random(0, 360)
    const returnAngle = 180
    const distance = random(500, 1000)

    // There are definitely less hacky ways to do this.
    setTimeout(() => {
      bugAngle.set(angle, { duration: 0 })
      bugAngleReturn.set(0, { duration: 0 })
      bugDistance.set(distance, { duration: crawlTime })

      setTimeout(() => {
        bugAngleReturn.set(returnAngle, { duration: turnTime })

        setTimeout(() => {
          bugDistance.set(distance * -1, { duration: crawlBackTime })

          setTimeout(() => moveBug(), crawlBackTime)
        }, turnTime)
      }, crawlTime + pauseTime)
    }, startTime)
  }
</script>

